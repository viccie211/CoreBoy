        public TileMap(byte[] vramData)
        {
            this._map0 = new int[MAP_SIDE, MAP_SIDE];
            int i = 0;
            for (int y = 0; y < MAP_SIDE; y++)
            {
                for (int x = 0; x < MAP_SIDE; x++)
                {
                    if (i < vramData.Length)
                    {
                        _map0[y, x] = vramData[i];
                    }
                    else
                    {
                        _map0[y, x] = 0;
                    }
                    i++;
                }
            }
        }

